package chararithdemo;

/**
 *
 * @author jamesfarrell
 */
public class CharArithDemo {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        char ch;
        
        ch = 'a';
        System.out.println("ch contains " + ch);
        
        ch++;
        System.out.println("ch is now " + ch);
        
        ch = 1350;
        System.out.println("ch is now " + ch);
        
    }
    
    
}
